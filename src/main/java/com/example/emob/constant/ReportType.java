package com.example.emob.constant;

public enum ReportType {
  FEEDBACK, // Phản hồi chung về xe (cảm nhận, trải nghiệm)
  COMPLAINT, // Khiếu nại về chất lượng hoặc dịch vụ liên quan đến xe
  DAMAGE, // Báo cáo xe bị hư hỏng / va chạm
  MAINTENANCE, // Yêu cầu bảo trì, bảo dưỡng
  PERFORMANCE, // Báo cáo vấn đề về hiệu suất (pin, động cơ, quãng đường,...)
}
